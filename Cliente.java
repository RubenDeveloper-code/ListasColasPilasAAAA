class Cliente {
    private int cuenta;
    private String nombre;
    private String apellidoPaterno;
    private double saldo;

    Cliente(int cuenta, String nombre, String apellidoPaterno, double saldo) {
        this.cuenta = cuenta;
        this. nombre = nombre;
        this.apellidoPaterno = apellidoPaterno;
        this.saldo = saldo;
    }
}
